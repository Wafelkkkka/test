# Задан список имен
names = ["Иван", "Петр", "Иван", "Сергей", "Петр", "Мария"]

# Получение имени от пользователя
user_name = input("Введите имя: ")

# Поиск имени в списке и подсчет количества появлений
count = 0
for name in names:
    if name == user_name:
        count += 1

# Вывод результата
if count > 0:
    print(f"Имя '{user_name}' найдено в списке {count} раз(а).")
else:
    print(f"Имя '{user_name}' не найдено в списке.")
